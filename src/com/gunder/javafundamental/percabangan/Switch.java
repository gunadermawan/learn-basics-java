package com.gunder.javafundamental.percabangan;

public class Switch {
    public static void main(String[] args) {
        int value = 5;
        switch (value) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            case 4:
                System.out.println("4");
                break;
            case 5:
                System.out.println("5");
                break;
            default:
                break;
        }
    }
}
